import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import { extname, join, parse } from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = fileURLToPath(new URL('../', import.meta.url));
const publicRoot = join(projectRoot, 'public');
const terminalRoot = join(publicRoot, 'terminal');
const outputPath = join(projectRoot, 'src/generated/portfolioContent.ts');
const terminalDirectoryNames = ['open_source', 'robotics', 'web_tooling'];
const rootScriptNames = ['contact.sh', 'theme.sh'];

async function readDirectoryFiles(directoryPath) {
  try {
    const entries = await readdir(directoryPath, { withFileTypes: true });
    return entries
      .filter((entry) => entry.isFile() && !entry.name.startsWith('.'))
      .map((entry) => entry.name)
      .sort((first, second) => first.localeCompare(second));
  } catch {
    return [];
  }
}

const description = (await readFile(join(publicRoot, 'description.txt'), 'utf-8')).trim();
const directories = await Promise.all(
  terminalDirectoryNames.map(async (name) => {
    const files = await readDirectoryFiles(join(terminalRoot, name));

    return {
      name,
      files: files.map((fileName) => ({
        name: fileName,
        displayName: `${parse(fileName).name}.md`,
        href: `/terminal/${name}/${fileName}`,
      })),
    };
  }),
);
const terminalTsxSources = {};

for (const directory of directories) {
  for (const file of directory.files) {
    if (extname(file.name) !== '.tsx') {
      continue;
    }

    const key = `${directory.name}/${file.name}`;
    terminalTsxSources[key] = {
      title: parse(file.name).name,
      source: await readFile(join(terminalRoot, directory.name, file.name), 'utf-8'),
    };
  }
}

const terminalManifest = {
  directories,
  rootFiles: rootScriptNames.map((name) => ({
    name,
    displayName: name,
    href: `/terminal/${name}`,
    extension: extname(name),
  })),
};

const generatedSource = `// This file is generated by scripts/generate-portfolio-content.mjs.
// Edit public/description.txt or public/terminal/**, then rerun the generator.

export const description = ${JSON.stringify(description, null, 2)};

export const terminalManifest = ${JSON.stringify(terminalManifest, null, 2)};

export const terminalTsxSources = ${JSON.stringify(terminalTsxSources, null, 2)};
`;

await mkdir(join(projectRoot, 'src/generated'), { recursive: true });
await writeFile(outputPath, `${generatedSource}\n`);
